/*
 * ============================================================
 *  笔记：fputc 底层原理 —— 为什么必须等待 TXE 标志位？
 * ============================================================
 *
 * 【一、printf 是怎么工作的？】
 *
 *   我们写的：printf("Hello");
 *
 *   printf 是 C 标准库函数，它内部会把字符串拆成一个个字节，
 *   然后每个字节都调用一次 fputc()。
 *   所以 printf("AB") 等价于：
 *       fputc('A');
 *       fputc('B');
 *
 *   fputc() 是我们自己重写的，目的是把字符通过 USART 串口发出去。
 *
 * ---------------------------------------------------------------
 *
 * 【二、USART 发送数据的硬件结构（最关键！）】
 *
 *   STM32 的 USART 发送端内部有【两个寄存器】：
 *
 *   ┌─────────────────────────────────────────────────────┐
 *   │  CPU                                                │
 *   │   │                                                 │
 *   │   │  USART_SendData(USART1, ch)                     │
 *   │   │  ↓ 把数据写入这里                               │
 *   │   ▼                                                 │
 *   │ ┌──────────────────┐                                │
 *   │ │  TDR             │  ← 发送数据寄存器              │
 *   │ │  (你写数据的地方) │    CPU 只能操作这个            │
 *   │ └────────┬─────────┘                                │
 *   │          │ 当移位寄存器空了，硬件自动把TDR的数据搬过去│
 *   │          ▼                                          │
 *   │ ┌──────────────────┐                                │
 *   │ │  移位寄存器       │  ← 真正发送数据的地方          │
 *   │ │  (硬件自动操作)   │    把数据一位一位移出到TX引脚   │
 *   │ └────────┬─────────┘                                │
 *   │          │ 115200波特率，每秒发115200个bit            │
 *   │          ▼                                          │
 *   │       TX 引脚 ──────────────────→ 电脑串口助手       │
 *   └─────────────────────────────────────────────────────┘
 *
 *   重点：CPU 只能把数据写入 TDR，
 *         TDR 的数据会【自动】搬到移位寄存器，
 *         移位寄存器负责一位一位地向外发送。
 *
 * ---------------------------------------------------------------
 *
 * 【三、TXE 标志位是什么？】
 *
 *   硬件提供了一个状态标志 TXE（TX Empty，发送寄存器空标志）：
 *
 *       TXE = 1  →  TDR 为空，数据已经被搬到移位寄存器了
 *                   此时可以安全写入下一个字节
 *
 *       TXE = 0  →  TDR 还有数据，还没被搬走
 *                   此时如果写入新数据，会把旧数据【覆盖丢失】！
 *
 * ---------------------------------------------------------------
 *
 * 【四、不加等待会发生什么？—— 数据丢失导致乱码】
 *
 *   USART_SendData() 这个库函数做的事情极其简单：
 *       把数据写入 TDR 寄存器，然后【立刻返回】，不等待任何事情。
 *
 *   115200 波特率下，发送 1 个字节需要约 87 微秒。
 *   但 CPU 主频 72MHz，执行一条写寄存器指令只需要约 0.01 微秒。
 *
 *   速度对比：
 *       CPU 写入速度：0.01 微秒/字节
 *       硬件发送速度：87  微秒/字节
 *       CPU 比硬件快 8700 倍！！！
 *
 *   所以不加等待的执行过程：
 *
 *   t=0.00us  fputc('A'): 写'A'到TDR，立刻返回
 *   t=0.01us  fputc('B'): 写'B'到TDR，'A'被覆盖！！！
 *   t=0.02us  fputc('C'): 写'C'到TDR，'B'被覆盖！！！
 *   ...
 *   t=87us    硬件才刚把第一个字节发出去（但已经是'C'了，'A''B'全丢了）
 *
 *   结果：电脑收到的全是乱的 → 乱码！
 *
 * ---------------------------------------------------------------
 *
 * 【五、加了等待之后 —— 正确流程】
 *
 *   while (USART_GetFlagStatus(USART1, USART_FLAG_TXE) == RESET);
 *
 *   这句话的意思：
 *       一直检查 TXE 标志，如果 TXE=0（寄存器还没空），就原地空转等待，
 *       直到 TXE=1（TDR 已空，数据已被硬件接管），才退出循环继续执行。
 *
 *   加了等待的执行过程：
 *
 *   t=0us     fputc('A'): 写'A'到TDR
 *             while循环等待...（CPU原地空转）
 *   t=87us    硬件把'A'从TDR搬到移位寄存器，TXE=1
 *             while检测到TXE=1，退出循环
 *   t=87us    fputc('B'): 写'B'到TDR（此时TDR已空，'A'安全！）
 *             while循环等待...
 *   t=174us   TXE=1，退出循环
 *   t=174us   fputc('C'): 写'C'到TDR
 *   ...
 *
 *   结果：每个字节都完整发出 → 正常显示！
 *
 * ---------------------------------------------------------------
 *
 * 【六、总结】
 *
 *   问：为什么要加 while(TXE == RESET) 等待？
 *
 *   答：因为 CPU 写寄存器的速度（0.01us）远快于硬件发送的速度（87us），
 *       不等待的话，CPU 会以极快的速度连续覆盖 TDR，
 *       导致只有最后一个字节被发出，前面全部丢失，显示乱码。
 *       加了等待，就是强制让 CPU 的速度降下来配合硬件，
 *       确保每一个字节都被硬件安全接管后，再写入下一个字节。
 *
 * ============================================================
 */

#include "Dri_Usart.h"

void Com_Debug_Init(void)
{
    Dri_usart_Init();
}

/*
 * fputc() —— printf 的底层输出函数
 *
 * printf 每输出一个字符都会调用这个函数。
 * 我们重写它，让字符通过 USART1 发送到电脑串口助手。
 *
 * 执行流程：
 *   1. USART_SendData()  把字节写入 TDR 发送寄存器
 *   2. while(TXE==RESET) 等待 TDR 被硬件搬空（即数据已交给移位寄存器）
 *   3. 返回，printf 再调用下一个字符的 fputc
 *
 * 如果去掉第2步：CPU太快，连续写入会覆盖上一个字节 → 乱码！
 */
int fputc(int ch, FILE *f)
{
    /* 第一步：把字节写入 TDR 发送寄存器 */
    USART_SendData(USART1, (uint8_t)ch);

    /*
     * 第二步：等待 TXE=1（TDR为空）
     *
     * TXE=0 (RESET)：TDR还有数据，硬件还没搬走，不能写新数据，继续等
     * TXE=1 (SET)  ：TDR已空，数据已被硬件接管，可以写下一个字节了
     *
     * 原理：115200波特率发1字节需87us，CPU写一次只需0.01us，
     *       不等待会以8700倍速度覆盖数据，导致数据全部丢失变成乱码。
     */
    while (USART_GetFlagStatus(USART1, USART_FLAG_TXE) == RESET);

    return ch;
}
